import turtle
tortuga = turtle.Turtle()
window = turtle.Screen()

def run():
    tortuga.forward(100)
    tortuga.right(90)

    tortuga.forward(100)
    tortuga.right(90)

    tortuga.forward(100)
    tortuga.right(90)

    tortuga.forward(100)
    tortuga.right(90)
    
    window.mainloop()
    

if __name__ == '__main__':
    run()